    function type() {
        if (!textElement) return;
        
        let currentText = texts[textIndex];
        
        if (isDeleting) {
            charIndex--;
            textElement.innerHTML = currentText.substring(0, charIndex);
            
            if (charIndex === 0) {
                isDeleting = false;
                textIndex = (textIndex + 1) % texts.length;
                setTimeout(type, 500);
            } else {
                setTimeout(type, 30);
            }
        } else {
            charIndex++;
            textElement.innerHTML = currentText.substring(0, charIndex);
            
            if (charIndex === currentText.length) {
                isDeleting = true;
                setTimeout(type, 2000);
            } else {
                setTimeout(type, 50);
            }
        }
    }
